class Province:

    def __init__(self, name, date, percent_vaccinated_total, percent_booster_total, total_vaccinations, total_fatalities, total_hospitalizations, total_cases):
        self.name = name
        self.date = date
        self.percent_vaccinated_total = percent_vaccinated_total
        self.percent_booster_total = percent_booster_total
        self.total_vaccinations = total_vaccinations
        self.total_fatalities = total_fatalities
        self.total_hospitalizations = total_hospitalizations
        self.total_cases = total_cases
